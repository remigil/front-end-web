				<div class="col-md-12 mb-5">
					<p style="text-align: justify;">Para mahasiswa menuntut adanya reformasi total termasuk turunnya Presiden Soeharto dari kursi kepresidenan. Tuntutan tersebut mendapatkan hasil dengan mundurnya presiden Soeharto dan diganti B.J. Habibie, yang sebelumnya menjabat Wakil Presiden. Presiden Habibie membentuk Kabinet Reformasi Pembangunan dan segera mempersiapkan pelaksanaan Pemilu untuk membentuk pemerintahan baru sesuai dengan kehendak rakyat.</p>
					<p style="text-align: justify;">Pada waktu terjadi demonstrasi dan kekacauan di Jakarta dan kota-kota lain di Indonesia. Polisi Lalu Lintas tetap aktif mengendalikan arus lalu lintas dalam melaksanakan tugas dibidang lalu lintas lainnya dengan penuh semangat, walaupun gelombang demonstrasi panjang cukup melelahkan Polisi Lalu Lintas tetap mewujudkan Kamtibcar Lantas.</p>
					<p style="text-align: justify;">Seiring dengan tuntutan demokratisasi dan supremasi hukum maka ditahun 1999 kedudukan Polri dipisahkan dari bagian ABRI menjadi di bawah Departemen Pertahanan dan Keamanan. Dengan terbitnya Ketetapan     Majelis     Permusyawaratan Rakyat Republik   Indonesia       Nomor : VI/MPR/2000 tanggal 18 Agustus 2000 tentang Pemisahan Tentara Nasional Indonesia dan Kepolisian Negara Republik Indonesia dan Nomor : VII/MPR/2000 tanggal 18 Agustus 2000 tentang Peran Tentara Nasional Republik Indonesia dan Peran Kepolisian Negara Republik Indonesia. Kedudukan Polri benar – benar mandiri dan terpisah dari peran pertahanan, seiring dengan perubahan dan pemisahan Organisasi Polri dari Organisasi ABRI maka disusun pula Undang – Undang Kepolisian sebagai perubahan dari Undang – Undang No 27 Tahun 1997 tentang Kepolisian Negara Republik Indonesia menjadi Undang – Undang No 2 Tahun 2002.</p>
					<p style="text-align: justify;">Pada tahun 2004 merupakan salah satu tonggak sejarah yang menunjukkan eksistensi Polantas yaitu dengan terbitnya Peraturan Pemerintah Nomor 31 Tahun 2004 tentang Penetapan Tarif PNBP yang berlaku dilingkungan Polri dimana 7 kewenangan yang diatur dalam PP tersebut 6 kewenangan milik Polantas. Dengan terbitnya PP No 31 Tahun 2004 sebagai pelaksanaan dari Undang – undang No 20 Tahun 1997 tentang Penerimaan Negara Bukan Pajak dilingkungan Polri, direvisi menjadi UU Nomor 9 Tahun 2018 tentang PNBP, diterbitkan PP 76 Tahun 2020 tentang Jenis dan Tarif PNBP yang berlaku pada Polri sebagai pengganti PP Nomor 50 Tahun 2010 dan PP 60 Tahun 2016 Tentang PNBP Polri, menghilangkan kesan Duplikasi tugas Pokok Polisi Lalu Lintas dengan Departemen Perhubungan, yaitu dimana Peran Polisi Lalu Lintas berada dalam tataran Keamanan Dalam Negeri melalui Registrasi dan Identifikasi kendaraan bermotor dan pengemudi yang merupakan ciri khas dari tugas – tugas Polisi secara Universal selaku aparat penegak hukum menggunakan Identifikasi dalam upaya pembuktian bahwa telah terjadi suatu tindak pidana, sedangkan Peran Departemen Perhubungan berada dalam tataran Regulator Transportasi Nasional.</p>
					<p style="text-align: justify;">Dengan pemberlakuan PP ini pula merupakan salah satu ciri khas yang dimiliki oleh fungsi teknis Polisi Lalu Lintas yaitu dapat memberi masukan kepada kas negara melalui biaya administrasi yang dipungut atas pelayanan Polri kepada masyarakat berdasarkan tarif yang telah ditentukan oleh Peraturan Pemerintah tersebut.</p>
					<p style="text-align: justify;">Perubahan sosial yang berjalan seiring dengan perkembangan globalisasi telah membawa pengaruh terhadap perubahan paradigma masyarakat. Menyadari dan memahami sepenuhnya keberadaan Polantas saat ini, diperlukan strategi ke depan yang sesuai dengan perubahan lingkungan strategik yang dihadapi Polantas. Perubahan Paradigma Polantas seiring dengan perubahan paradigma Polri yang merupakan refleksi dan tuntutan terhadap peningkatan peran dan tugas Polantas yang semakin kompleks di tengah – tengah masyarakat. Tuntutan akan Polantas yang Profesional dan Proporsional yang bercirikan Perlindungan, Pengayoman, Pelayanan kepada masyarakat, Penegakan Demokrasi dan Hak Asasi Manusia dalam rangka kepastian hukum dan terwujudnya kamtibcar lantas menuntut reposisi atas kedudukan serta pemulihan fungsi dan peranannya.</p>
					<p style="text-align: justify;">Dalam rangka mewujudkan tuntutan tersebut Direktorat Lalu Lintas telah menyusun Program Pembangunan Polisi Lalu Lintas 5 (Lima) tahun kedepan dan perubahan struktur organisasi menjadi organisasi yang berada langsung di bawah Kapolri, dengan maksud dan tujuan agar Masyarakat pemakai jalan memahami dan yakin kepada Polantas sebagai pelindung, pengayom dan pelayan masyarakat dalam kegiatan Pendidikan Masyarakat lalu lintas, penegakan hukum lalu lintas, pengkajian masalah lalu lintas, registrasi dan identifikasi kendaraan bermotor dan pengemudi, demi tercapainya keamanan, ketertiban dan kelancaran lalu lintas.</p>
					<p style="text-align: justify;">Sejak reformasi birokrasi di lingkungan Polri terus bergulir, termasuk reformasi instrumental, struktural, dan kultural. Reformasi instrumental akan termasuk kendaraan dan teknologi pendukung tugas Polri di lapangan. Karena diharapkan tugas Polri menjadi lebih baik dibanding sebelumnya, sehingga peralatan yang dimiliki agar berfungsi dengan baik agar dapat membantu kinerja polisi di lapangan. Kemudian berdasarkan Peraturan Presiden Nomor 52 Tahun 2010 tanggal 4 Agustus 2010 DitLantas Polri Menjadi Korps Lalu Lintas Polri (Korlantas Polri). Korlantas Polri berkedudukan langsung Kapolri, untuk membina dan menyelenggarakan fungsi Lalu Lintas dibawah termasuk pendidikan masyarakat, penegakkan hukum, pengkajian masalah lalu lintas, registrasi dan pengemudi kendaraan bermotor serta patroli jalan raya. Berdasarkan Perkap No 6 Tahun 2017 Tentang Susunan Organisasi dan Tata Kerja Satuan Organisasi pada Tingkat Markas Besar Kepolisian Negara Republik Indonesia, struktur organisasi Korlantas Polri dikembangkan adanya Kakorlantas berpangkat Irjen Pol dan penjabat utama ditambah 3 Direktorat dan 3 Kabag yaitu Direktur Kamsel, Direktur Gakkum, Direktur Regident, Kabagops, Kabagrenmin dan Kabag TIK Korlantas Polri, sampai dengan sekarang ini.</p>
					
