				</div>
				<div class="col-md-12 mb-5">
					<p style="text-align: justify;">Untuk mengimbangi perkembangan lalu lintas yang terus meningkat, maka pemerintah Hindia Belanda memandang perlu membentuk wadah Polisi tersendiri yang khusus menangani lalu lintas, sehingga pada tanggal 15 Mei 1915, dengan Surat Keputusan Direktur Pemerintah Dalam Negeri No. 64/a lahirlah satu organ Polisi Lalu Lintas dalam tubuh Polisi Hindia Belanda. Dalam organ Polisi pada waktu itu ada empat bagian, yaitu bagian sekretaris, bagian serse, bagian pengawas umum dan bagian lalu lintas. Pada mulanya bagian lalu lintas di sebut Voer Wesen, sebagai jiplakan dari bahasa Jerman “Fuhr Wessen” yang berarti pengawasan lalu lintas. Organ ini terus disempurnakan, diberi nama asli dalam bahasa Belanda Verkeespolitie. artinya Polisi Lalu Lintas.</p>
					<p style="text-align: justify;">Selama penjajahannya Pemerintah Hindia Belanda aktif membuat aturan – aturan mengenai Polisi Lalu Lintas. Pada tanggal 23 Februari 1933 dikeluarkan Undang – undang lalu lintas jalan dengan nama : DE Wegverkeers Ordonantie (stadblaad No68). Undang – undang ini terus disempurnakan tanggal 1 Agustus 1933 (stadblaad No 327). Tanggal 27 Februari 1936 ( stadblaad No 83), tanggal 25 Nopember 1938 ( stadblaad No 657 dan terakhir tanggal 1 Maret 1940 (stadblaad No 72).</p>
					<p style="text-align: justify;">Tentu kesungguhan pemerintah Hindia Belanda bukan saja membuat undang – undang tetapi juga mengembangkan jaringan jalan dalam kota maupun antar kota, organisasi dan kader – kader Polisi Lalu Lintas terus di bentuk.</p>
				</div>
				<div class="col-md-6">
					<h4 class="mt-3 mb-3"><b>2. Penjajahan Jepang</b></h4>
					<p style="text-align: justify;">Setelah Belanda menyerah kepada Jepang, dalam perang Asia Timur Raya maka pemerintahan Indonesia dikuasai oleh bala tentara Jepang. Segala aspek kehidupan ditentukan oleh kekuasaan Militer. Bidang lalu lintas juga diatur dan dikuasasi dengan cara militer. Dalam organ kepolisian hanya ada organ Kempetai ( Polisi Militernya Jepang). Demikian juga mengenai pengaturan lalu lintas jalan dilakukan oleh Polisi Militer. Sedangkan Polisi Lalu Lintas tidak nampak dan tidak banyak diketahui prang pada masa itu, anggota Polisi Lalu Lintas yang bersedia bekerja sama dengan Jepang dan sudah berpengalaman sebelumnya mendapat tugas membentuk registrasi kendaraan bermotor terutama yang di tinggal pemiliknya karena suasana Jepang.</p>

				</div>
				<div class="col-md-6">
					<img src="<?= base_url()?>assets/fe/sejarah/sejarah3.jpg" alt="" style="width: 100%; height:300px">
					<p class="text-center">sumber : <a href="https://www.yuksinau.id/pendudukan-jepang-di-indonesia/">https://www.yuksinau.id/pendudukan-jepang-di-indonesia/</a></p>
				</div>
				<div class="col-md-12">
					<p style="text-align: justify;">Gemblengan dan penindasan militerisme Jepang disamping menimbulkan banyak korban jiwa, namun pengorbanan tersebut tidak sia – sia karena di sisi lain mendorong semangat patriot di dada Bangsa Indonesia. Hal ini dibuktikan setelah bala tentara Jepang menyerah kepada sekutu dengan di bomnya kota Hiroshima dan Nagasaki, dengan serentak Bangsa Indonesia bergerak dan memproklamirkan kemerdekaan. Dari segala penjuru tanah air dan dari segala lapisan masyarakat, baik petani, pedagang, pegawai negeri, polisi, prajurit peta bersama – sama bahu membahu bergerak menyambut kemerdekaan yang telah diproklamirkan tanggal 17 Agustus 1945.</p>
					<p style="text-align: justify;">Polisi ( Polantas ) dengan perlengkapan yang ada, senjata, kendaraan dan lainnya siap mengamankan masyarakat dalam menyambut hari gembira yaitu Proklamasi. Dengan kendaraan yang ada Polisi Lalu Lintas mengamankan dan mengawal para pejabat / politikus yang akan menuju ke gedung Proklamasi di Jl. Pegangsaan Timur serta ke lapangan Gambir guna menyambut proklamasi yang bersejarah itu.</p>
				</div>

