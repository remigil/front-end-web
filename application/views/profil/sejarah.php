				<div class="col-md-6">
					<h4 class="mt-3 mb-3"><b>3. Periode 1959 - 1965</b></h4>
					<p style="text-align: justify;">Dekrit Presiden 5 Juli 1959 secara fundamental membawa sistem politik dan ketatanegaraan berubah yaitu kembali ke UUD 1945 dengan sistim kabinet Presidentil, Presiden disamping sebagai Kepala Negara juga sekaligus sebagai Kepala Pemerintahan. Presiden juga menjabat sebagai Panglima Tertinggi ABRI. Dengan kembali ke UUD 1945 membawa perubahan baik struktural maupun strategis, maka istilah kementerian diganti departemen, seperti kementerian pertahanan menjadi Departemen Pertahanan Nasional.</p>
					<p style="text-align: justify;">Selanjutnya dengan Keppres No. 15 tahun 1963 Kepala Staf Angkatan berstatus sebagai menteri / Panglima Angkatan memegang kekuasaan tertinggi pada angkatannya dan bertanggung jawab langsung kepada Panglima Tertinggi / Presiden R.l.</p>
