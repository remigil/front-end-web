					<p style="text-align: justify;">Untuk menyusun organisasi kepolisian maka dikeluarkan Surat keputusan Men Hankam Pangab No. Kep. A./385A/1111970 tentang Pokok-pokok Organisasi dan Prosedur Kepolisian Negara R.l. Sebagai penjabarannya dikeluarkan Surat Keputusan Kapolri No.Pol. 113/SK/1970 tanggal 17 September 1970 tentang Organisasi Staf Umum dan Staf Khusus dan Badan-badan pelaksana Polri, maka lahirlah organisasi baru di lingkungan Polri. Demikian juga di kalangan Polisi Lalu Lintas Pusat.</p>
					<p style="text-align: justify;">Dua tahun sebelum surat keputusan ini (tahun 1968) di tingkat pusat dibentuk Pusat Kesatuan Operasi Lalu Lintas (Pusatop Lantas), dengan komandannya KBP Drs. U.E. Medelu. Dengan keluarnya SK tersebut berubah kembali menjadi Direktorat Lalu Lintas tahun 1970, yang merupakan salah satu unsur Komando Utama Samapta Polri, sehingga kemudian disebut Direktorat Lalu Lintas Komapta.</p>
					<p style="text-align: justify;">Pada periode ini dibentuk Patroli Jalan Raya (PJR) oleh Mabes Polri, meski sebenarnya pembentukan Patroli Jalan Raya sudah dilakukan di Kepolisian Daerah, namun baru tahun 1966 dibentuk secara resmi berdasarkan instruksi Men Pangab No. 31/lnstr/MK/1966. Pembentukan Kesatuan PJR ini memang didasari dengan pertimbangan-pertimbangan yang matang. Dalam pelaksanaan tugasnya anggota PJR dituntut untuk selalu siaga dan berpedoman kepada motto courtesy, protection, and service (ramah tamah perlindungan dan pelayanan). Detasemen PJR ini dipimpin oleh seorang komandan yang ditunjuk oleh Direktur Lalu Lintas dibawah pengawasan Kepala Dinas Pengawasan Direktorat Lalu Lintas.</p>
					<p style="text-align: justify;">Permasalahan lalu lintas mulai terasa meningkat ditandai meningkatnya frekwensi pelanggaran lalu lintas. Nampaknya masalah ini cukup merisaukan, terlebih para aparat penegak hukum. Dipandang dari segi sarana penindakan tampak memang kurang efektif. Tahun 1969 dibentuk team untuk merumuskan sistem penindakan pelanggaran lalu lintas yang praktis dan cepat.</p>
					<p style="text-align: justify;">Pada tanggal 11 Januari 1971 lahir Surat Keputusan Bersama antara Ketua Mahkamah Agung No. 001/KMA/71, Jaksa Agung No. 002/DA/1971, Kepala Kepolisian R.l No. 4/SK/Kapolri/71 dan Menteri Kehakiman No. JS/1/21 yang mengesahkan berlakunya Sistem Tilang untuk pelanggaran lalu lintas. Dari Pihak Polri Tim perumus diwakili oleh Jenderal Memet Tanu Miharja, Brigjen Pol. Drs. VE. Madelu, Letkol Pol Drs. Basirun. Mulai tahun 1971 mulailah pelanggaran lalu lintas ditindak dengan tiket system yang dikenal dengan bukti pelanggaran disingkat tilang.</p>
					<p style="text-align: justify;">Tanggal 29 Maret 1969 didirikan Pusat Pendidikan Lalu Lintas (Pusdik Lantas) yang berkedudukan di jalan MT. Haryono Jakarta Selatan, masih satu kantor dengan Direktorat Lalu Lintas Polri. Kemudian pada tahun 1985 dipindahkan ke Serpong Tangerang Jawa Barat sampai saat ini sejak tahun 1969 pendidikan lalu lintas untuk Perwira dan Bintara Lalu Lintas dapat dilaksanakan secara teratur.</p>
					<p style="text-align: justify;">Berdasarkan Surat Keputusan Men Hankam No. Kep/15/IV/1976 tanggal   13 April 1976, Skep Kapolri No. Pol. Skep/507V111/1977, dan Skep Kapolri No. Pol. Skep/53/VII/1977 di tingkat Mabak terdapat dua unsur lalu lintas. Pertama ; Dinas Lalu Lintas Polri yang berkedudukan sebagai Badan Pelaksana Pusat dibawah yang sehari-harinya dikoordinasi oleh Deputy Kapolri dengan tugas pokok membantu Kapolri untuk menyelenggarakan segala kegiatan dan pekerjaan di bidang pencegahan, penanggulangan terhadap terjadinya gangguan/ancaman terhadap Kamtibmas di bidang Lantas dan menindak apabila diperlukan dalam rangka kegiatan atau operasional Kepolisian, Kedua : pusat system senjata Lalu Lintas Polri yang berkedudukan dibawah Danjen Kobang Diklat Polri dengan tugas pokok menyelenggarakan segala usaha kegiatan mengenai pengembangan taktik dan teknik system senjata serta pendidikan latihan di bidang fungsi teknis lalu lintas Polri dalam rangka system Kamtibmas, serta tugas lain yang dibebankan padanya. Pusdik lantas kedudukannya dibawah Pusenlantas sebagai penyelenggara pendidikan. Dan secara organisatoris terpisah dari Dinas Lalu Lintas.</p>
					<p style="text-align: justify;">Selanjutnya berdasarkan Keputusan Pangab No.Kep/11/P/III/1984 tentang Pokok-pokok Organisasi dan Prosedur Kepolisian Negara R.l, dan Keputusan Kapolri No. Pol: Kep/09/X/1984 tanggal 30 Oktober 1984, Pusdik lantas kembali berada di bawah Direktorat Pendidikan Polri.</p>
					<p style="text-align: justify;">Pada tahun 1984 dengan Surat keputusan Pangab No. Kep/11/P/ll 1/1984 tanggal 31 Maret 1984 tentang Pokok-pokok Organisasi dan Prosedur Kepolisian R.l, Dinas Lalu Lintas Polri dirubah dan diperkecil struktur organisasinya menjadi Sub Direktorat Lalu Lintas Polri di bawah Direktorat Samapta Polri bersama-sama dengan Subdirektorat Polisi Perairan, Polisi Udara dan Satwa Polri.</p>
					<p style="text-align: justify;">Pada tahun 1991 tepatnya tanggal 21 Nopember 1991 Subdirektorat Lalu Lintas dikembangkan kembali organisasinya menjadi Direktorat Lalu Lintas Polri berkedudukan di bawah Kapolri yang sehari-harinya dikoordinasikan oleh Deputi Operasi Kapolri.</p>
					
