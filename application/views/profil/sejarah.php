				<div class="col-md-6">
					<h4 class="mt-3 mb-3"><b>1. Penjajahan Belanda</b></h4>
					<p style="text-align: justify;">Sejarah lalu lintas di Indonesia tidak lepas dari perkembangan teknologi automotif dunia, yang berawal dari penemuan mesin dengan bahan bakar minyak bumi. Pada Jaman revolusi di Eropa terutama akhir abad 19 mobil dan sepeda motor mulai berkembang banyak diproduksi. Industri Mobil dipelopori oleh Benz yang perusahaannya berkembang sejak tahun 1886. Pemerintah Hindia Belanda yang saat itu menjajah Indonesia mulai membawa mobil dan sepeda motor masuk ke Indonesia. Mulai munculnya aktivitas lalu lintas kendaraan bermotor di Indonesia. Ketika mobil dan sepeda motor bertambah banyak Pemerintah Hindia Belanda mulai merasa perlu mengatur penggunaannya. Peraturan pertama di keluarkan pertama kali pada tanggal 11 Nopember 1899 dan dinyatakan berlaku tepat tanggal 1 Januari 1900. Bentuk peraturan ini adalah Reglement (Peraturan Pemerintah) yang disebut Reglement op gebruik van automobilen ( stadblaad 1899 no 301 ). Sepuluh tahun kemudian pada tahun 1910 dikeluarkan lagi Motor Reglement ( stb 1910 No.73 ). Dengan demikian pemerintah Hindia Belanda telah memperhatikan masalah lalu lintas di jalan dan telah menetapkan tugas Polisi di bidang lalu lintas secara represif.</p>
					<p style="text-align: justify;">Organ kepolisian sendiri telah ada lebih awal sejak jaman VOC, namun baru di pertegas susunannya pada masa pemerintah Gubernur Jenderal Sanford Raffles, masa pendudukan Inggris. Kantor – kantor Polisi baru ada di beberapa kota – kota besar seperti Jayakarta, Semarang, Surabaya, yang umurnya dipegang oleh Polisi Belanda pada intinya.</p>
				</div>
				<div class="col-md-6">
					<img src="<?= base_url()?>assets/fe/sejarah/sejarah1.jpg" alt="" style="width: 100%; height:300px">
					<p class="text-center">Pasukan Veldpolitie di Malang, Jawa Timur, sekitar tahun 1930. Sumber : <a href="https://www.wikipedia.org">Wikipedia.org</a></p>
