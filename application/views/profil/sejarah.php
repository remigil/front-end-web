					<h4 class="mt-3 mb-3"><b>5. Periode 1998 - Sekarang</b></h4>
					<p style="text-align: justify;">Pada pertengahan tahun 1997, diawali dengan merosotnya nilai tukar rupiah terhadap dolar Amerika, Indonesia dilanda resesi dan krisis moneter dan berkembang menjadi krisis ekonomi. Masyarakat yang dipelopori oleh mahasiswa melakukan demonstrasi menyatakan tidak percaya lagi dengan pemerintahan orde baru di bawah kepemimpinan Presiden Soeharto.</p>
					<p style="text-align: justify;">Pada tanggal 12 Mei 1998 terjadi peristiwa berdarah dengan meninggalnya 4 orang mahasiswa peserta demonstrasi di depan Universitas Trisakti Jakarta, hal ini yang memicu gerakan demonstrasi mahasiswa yang lebih besar dan menguasai gedung DPR/MPR R.l. Peserta demonstrasi tidak terbatas pada mahasiswa Ibu Kota Jakarta tetapi di semua kota di seluruh Indonesia.</p>
				</div>
