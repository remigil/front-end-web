				</div>
				<div class="col-md-6">
					<h4 class="mt-3 mb-3"><b>1. Periode 1945 - 1950</b></h4>
					<p style="text-align: justify;">Pada masa Proklamasi ini sudah nampak kegiatan Polisi Lalu Lintas setiap ada kegiatan di jalan raya. Banyak tokoh – tokoh polisi yang ikut aktif dalam mempersiapkan hari proklamasi bersama dengan tokoh – tokoh lainnya. Tokoh – tokoh Polisi tersebut antara lain R.S. Soekanto dan R. Sumanto.</p>
					<p style="text-align: justify;">Tanggal 19 Agustus 1945 Panitia Persiapan Kemerdekaan Indonesia (PPKI) menetapkan bahwa Polisi termasuk di dalam lingkungan Departemen Dalam Negeri. Hal ini berarti Jawatan Kepolisian Negara, secara administrasi mempunyai kedudukan yang sama dengan Dinas Polisi Umum dari Pemerintah Hindia Belanda.</p>
				</div>
				<div class="col-md-6">
					<img src="<?= base_url()?>assets/fe/sejarah/sejarah4.jpg" alt="" style="width: 100%; height:300px">
					<p class="text-center">Kapolri pertama Indonesia R.S Soekanto (duduk paling kiri) <a href="https://www.polri.go.id">(polri.go.id)</a></p>
				</div>
				<div class="col-md-12 mb-5">
					<p style="text-align: justify;">Ketentuan tersebut diperkuat oleh suatu maklumat pemerintah tanggal     1 Oktober 1945 yang ditanda tangani oleh Menteri Dalam Negeri, Menteri Kehakiman dan Jaksa Agung yang telah menyatakan bahwa semua kantor kejaksaan termasuk dalam lingkungan Departemen Kehakiman sedangkan semua kantor Badan Kepolisian masuk dalam lingkungan Departemen Dalam Negeri.</p>
					<p style="text-align: justify;">Tanggal 29 Desember 1945 Presiden mengangkat dan menetapkan R.S. Soekanto sebagai Kepala Kepolisian Negara R.l yang pertama. Pengangkatan ini disamping suatu kehormatan juga tantangan, dimana pada masa itu bangsa Indonesia menghadapi perang melawan Belanda. Kekurangan, keterbatasan serta kesulitan yang datang silih berganti menjadi tantangan tersendiri.</p>
					<p style="text-align: justify;">Sehari kemudian tepatnya tanggal 30 September 1945 Belanda dengan dipimpin oleh Van Der Plas membujuk Polisi Republik Indonesia berunding segitiga dengan Belanda dan Jepang. Setelah ada ijin dari Pimpinan Polisi R.l baru mau menghadiri perundingan tersebut. Dalam perundingan itu Van Der Plas memerintahkan agar Polisi tetap bekerja dengan pangkat yang ada. Apabila cakap akan tetap dipertahankan dan apabila tidak, maka akan diberhentikan. Sedangkan perwakilan Polisi R.l, Sosrodanu Kusumo memberikan masukan agar Belanda terus berhubungan dengan pemerintah R.l. Dari peristiwa itu, jelas bahwa Belanda tetap ingin menguasasi Kepolisian R.l.</p>
					<p style="text-align: justify;">Tanggal 29 Desember 1945 kantor Polisi Jakarta tiba – tiba di serbu serentak oleh tentara sekutu (Inggris ). Semua anggota Polisi di kumpulkan di Kantor Besar Polisi, baru setelah beberapa hari dilepaskan kembali.</p>
					<p style="text-align: justify;">Bulan Januari 1946 dibentuk Civil Police dimana Polisi Indonesia dan Polisi Belanda dipisahkan, sedangkan Inggris sebagai penengahnya. Hubungan antara kantor Polisi Pusat dengan Polisi Daerah pada bulan pertama praktis tidak ada. Hanya secara insidentil Kepala Kepolisian mengirim kurir – kurir ke daerah untuk meneruskan instruksi.</p>
					<p style="text-align: justify;">Pada periode ini walaupun anggota Polisi banyak yang meninggalkan tugas dan ikut bergerilya di hutan – hutan namun tugas kepolisian termasuk lalu lintas tetap berjalan, walau hanya dengan peralatan yang sederhana dan masih sangat terbatas. Pada bulan Februari 1946 Jawatan Kepolisian yang tergabung di dalam Departemen Dalam Negeri memindahkan kantor pusat / kedudukannya di Purwokerto.</p>
					<p style="text-align: justify;">Karena kesulitan yang dihadapi oleh Jawatan Kepolisian pada waktu itu sedangkan mereka sangat dibutuhkan maka pada tanggal 1 Juli 1946 dengan Penetapan Pemerintah No. 11 /SD tahun 1946 Jawatan Kepolisian Negara dipisahkan dari Departemen Dalam Negeri dan menjadi Jawatan sendiri dibawah Perdana Menteri, tanggal ini selanjutnya di jadikan tanggal kelahiran dan dijadikan hari Bhayangkara.</p>
					<p style="text-align: justify;">Pada periode ini Jawatan Kepolisian Negara, mulai membenahi wadah – wadah, organisasi kepolisian walaupun menghadapi berbagai kendala. Usaha – usaha yang telah dilakukan antara lain:</p>
					<ol>
						<li>Menyusun suatu Jawatan pusat dengan bagian – bagiannya. Tata Usaha Keuangan, Perlengkapan, Organisasi Pengawasan Aliran Masyarakat dan Pengusutan Kejahatan.</li>
						<li>Menciptakan peraturan – peraturan mengenai pakaian dinas, tanda pangkat, tata tertib dan tata susila, baris berbaris dan lain – lain.</li>
						<li>Menyusun kembali Polisi Lalu Lintas, dengan tugas lain yang pada saat dan waktu mendatang diperlukan.</li>
					</ol>
					<p style="text-align: justify;">Dasar penyusunan kembali Polisi Lalu Lintas tersebut secara resmi tidak diketahui, namun penyusunan ini mudah disebabkan keadaan lalu lintas yang memang masih belum seramai seperti sekarang ini. Jumlah kendaraan di masa pendudukan Jepang masih sangat sedikit. Sisa kendaraan dari masa pendudukan Jepang yang ditinggal sedikit menjadi semakin berkurang, karena usia dan suku cadang yang tidak tersedia atau sulit mencari gantinya. Pada periode ini masalah lalu lintas belum mendapat perhatian yang sungguh – sungguh.</p>

				</div>

				<div class="col-md-6">
					<h4 class="mt-3 mb-3"><b>2. Periode 1950 - 1959</b></h4>
					<p style="text-align: justify;">Pada periode ini lahir Seksi Lalu Lintas dalam wadah Polisi Negara R.l. Sebenarnya usaha -usaha penyusunan kembali organisasi Polisi Indonesia itu sudah ada sejak diangkatnya Kepala Jawatan Kepolisian Negara namun usaha itu terhenti pada saat pecah perang kemerdekaan ke dua ( Clash II)</p>
					<p style="text-align: justify;">Setelah penyerahan kedaulatan Negara R.l tanggal 29 Desember 1949 baru dapat dilanjutkan kembali. Pimpinan Polisi di daerah pendudukan yang dipegang oleh kader – kader Belanda di ganti oleh kader – kader Polisi Indonesia. Hanya dalam mereorganisasi Kepolisian Indonesia dinamakan Jawatan Kepolisian dan pada masa terbentuknya Negara Kesatuan tanggal 17 Agustus 1950 berubah namanya menjadi Jawatan Kepolisian Negara.</p>
					<p style="text-align: justify;">Karena kemajuan dan perkembangan masyarakat yang mulai perlu diantisipasi maka organisasi Polisi memerlukan penyesuaian agar dapat mewadahi dan menangani pekerjaan dengan cepat. Untuk itu diperlukan spesialisasi. Sehingga tanggal 9 Januari 1952 dikeluarkan order KKN No.6 / IV / Sek / 52. Tahun 1952 mulai pembentukan kesatuan – kesatuan khusus seperti Polisi Perairan dan Udara serta Polisi Lalu Lintas yang dimasukkan dalam pengurusan bagian organisasi. Untuk Polisi Lalu Lintas di wilayah Jakarta Raya merupakan bagian tersendiri yang mempunyai rumusan tugas sebagai berikut:</p>
					<ul>
						<li>Mengurus lalu lintas</li>
						<li>Mengurus kecelakaan lalu lintas</li>
						<li>Pendaftaran nomor bewijs</li>
						<li>Motor Brigade keramaian</li>
						<li>Komando pos radio dan bengkel</li>
					</ul>
				</div>
				<div class="col-md-6">
					<img src="<?= base_url()?>assets/fe/sejarah/sejarah5.jpg" alt="" style="width: 100%; height:750px">
					<p class="text-center">Kelompok Petugas dari Kepolisian RI yang mengawal para delegasi peserta KAA Bandung 1955.</p>
				</div>
				<div class="col-md-12 mb-5">
					<p style="text-align: justify;">Dengan kemajuan teknologi dan perkembangan lalu lintas yang semakin pesat Kepala Jawatan Kepolisian Negara memandang perlu untuk membangun wadah yang konkrit bagi penanganan -penanganan masalah lalu lintas. Oleh karenanya maka pada tanggal 22 September 1955. Kepala Jawatan Kepolisian Negara mengeluarkan Order No 20 / XVI / 1955 tanggal 22 September 1955, tentang Pembentukan Seksi Lalu Lintas Jalan, pada tingkat pusat yang taktis langsung di bawah Kepala Kepolisian Negara. Maka saat itu dikenal istilah lalu lintas jalan untuk pertama kalinya, yang mempunyai rumusan tugas sebagai berikut:</p>
					<ul>
						<li>Mengumpulkan segala bahan yang bersangkutan dengan urusan lalu lintas jalan</li>
						<li>Memelihara / mengadakan peraturan, peringatan dan grafik tentang kecelakaan lalu lintas , jumlah pemakai jalan, pelanggaran lalu lintas jalan.</li>
						<li>Mengadakan pengawasan atas pelaksanaan perundang – undangan lalu lintas jalan dan menyiapkan instruksi guna pelaksanaan di berbagai daerah.</li>
						<li>Melayani sebab – sebab kecelakaan lalu lintas jalan di berbagai tempat di Indonesia, dan menyiapkan instruksi dan petunjuknya guna menurunkan / mengurangi angka kecelakaan lalu lintas.</li>
						
					</ul>
					<p style="text-align: justify;">Tahun 1956, di tiap kantor Polisi Propinsi dibentuk Seksi Lalu Lintas dengan Order Kepala Kepolisian Negara No. 20 / XIII /1956 tanggal 27 Juli 1956 kemudian di kesatuan – kesatuan / kantor -kantor Polisi Karesidenan, selanjutnya pada tingkat Kabupaten di bentuk pula seksi – seksi Lalu lintas dengan berdasar pada Order KKN tersebut.</p>
					<p style="text-align: justify;">Kegiatan dan peristiwa penting dalam tugas Polantas pada periode ini adalah pengamanan Konferensi Asia Afrika yang berlangsung di Bandung bulan April 1955, konferensi dihadiri delegasi dari berbagai negara Asia Afrika. Konferensi mempunyai arti penting baik bagi Indonesia maupun negara -negara Asia Afrika dalam rangka mengubah pandangan dan nasib bangsa – bangsa Asia Afrika. Polisi Lalu Lintas berperan aktif memberikan perlindungan, keamanan, keselamatan jalan dan kelancaran lalu lintas. Mengawal dan mengamankan jalan di tempat – tempat yang dilalui para tamu negara, di lokasi konferensi maupun tempat – tempat lainnya yang dikunjungi. Tugas pengamanan ini merupakan tugas yang sangat berat bagi Polisi Lalu Lintas. Bahkan untuk tugas ini Polisi Lalu Lintas mengerahkan tenaga secara besar – besaran dari seluruh Jawa. Peristiwa ini patut di catat dalam sejarah Polisi. Dimana tugas mengabdi pada bangsa dan negara ini berhasil dan sukses.</p>
					<p style="text-align: justify;">Pada peristiwa Cikini dimana Presiden Soekarno mendapat serangan granat dari komplotan tidak bertanggung jawab, saat menghadiri ulang tahun Perguruan Cikini. Dalam peristiwa ini banyak jatuh korban. Dua anggota Polantas yang saat itu mengawal rombongan dari tempat tersebut sebelum sempat melapor telah didahului dengan lemparan granat ke arah Presiden tetapi tidak mengenai sasaran, namun malah mengenai Aipda Muhammad dan Bripda Ahmad sehingga gugur dalam melaksanakan tugas mulia tersebut. Atas jasa dan pengorbanan kedua anggota Polantas tersebut pemerintah memberikan penghargaan dan jenazahnya dimakamkan di Taman Makam Pahlawan Kalibata Jakarta. Dua peristiwa tersebut dan beberapa peristiwa lain patut dicatat bahwa betapa besar tugas Polisi Lalu Lintas yang telah dilaksanakan dengan tabah, tekun dan penuh pengabdian.</p>
					<p style="text-align: justify;">Pada periode ini telah diadakan beberapa kegiatan untuk perbaikan lalu lintas antara lain menyangkut engineering misalnya:</p>
					<ul style="text-align: justify;">
						<li>Diperkenalkannya istilah pulau – pulau jalan oleh Komisaris Besar Untung Margono untuk pertama kalinya di Indonesia. Pada pembuatan pulau – pulau ini diadakan kerja sama dengan Departemen Pekerjaan Umum dengan maksud untuk kelancaran lalu lintas.</li>
						<li>Penegasan kembali pemasangan rambu – rambu lalu lintas yang mulai nampak     adanya penyimpangan – penyimpangan, baik bentuk, warna maupun   pemasangannya. Untuk itu pemasangan rambu perlu dasar hukum     yang kuat karena   Indonesia sudah   menjadi anggota Convention on Road Traffic.</li>
						<li>Dimulainya pendidikan lalu lintas pada anak – anak sekolah agar anak –   anak sejak kecil sudah kenal dengan masalah – masalah lalu lintas. Maka dibentuklah Badan Keamanan Lalu Lintas (BKLL) untuk pertama kali di   Jakarta pada tahun 1953 dengan maksud :
							<ol>
								<li>Menanamkan rasa tanggung jawab akan keselamatan lalu lintas terhadap orang   lain dan terhadap umum.</li>
								<li>Membantu menjaga keamanan lalu lintas dan mengurangi kecelakaan terutama yang melibatkan anak – anak sekolah</li>
								<li>Berusaha mewujudkan cita – cita masyarakat yang mempunyai disiplin lalu lintas yan tinggi sopan santun dan berpengetahuan lalu lintas yang luas.</li>
							</ol>
						</li>
					</ul>

