				<div class="col-md-12 mb-5">
					<p style="text-align: justify;">Didalam tubuh kepolisian terjadi perubahan yang mendasar yaitu dari Jawatan Kepolisian Negara berubah menjadi Angkatan Kepolisian Republik Indonesia (AKRI) karena AKRI tetap konsekuen dan konsisten pada tugasnya, maka pada jaman dicanangkannya Trikora, Dwikora maupun penumpasan gerakan pengacau keamanan tetap aktif pada kancah tugas perjuangan. Disamping itu kegiatan pejuang – pejuang AKRI dalam hal ini Polantas tetap setia dan berbakti kepada Negara.</p>
					<p style="text-align: justify;">Pada tanggal 23 Oktober 1959 dengan peraturan sementara dari Menteri / KKN di keluarkan peraturan sementara Menteri /KKN No. 2.PRA/MK/1959 tentang Susunan dan Tugas Markas Besar Polisi Negara. Dengan berdasar pada peraturan ini status Seksi Lalu Lintas Jalan di perluas menjadi Dinas Lalu Lintas dan Polisi Negara Urusan Kereta Api (PNUK). Tugas – tugas lainnya antara lain :</p>
					<ol>
						<li>Mengatur pemberian jaminan bantuan kepada instansi – instansi yang membutuhkan bantuan Polisi bagi kelancaran dan keamanan lalu lintas daratan.</li>
						<li>Kedua mengatur pelaksanaan pemeliharaan kelancaran dan keamanan lalu lintas di daratan termasuk Kereta Api.</li>
						<li>Memberi nasehat dan saran – saran mengenai soal – soal lalu lintas di daratan kepada instansi – instansi yang membutuhkan.</li>
					</ol>
					<p style="text-align: justify;">Kepala Dinas Lalu Lintas / PNUK adalah Ajun Komisaris Besar Polisi Untung Margono yang menggantikan Komisaris Besar Polisi H.S Djajoesman. Lahirnya Undang – Undang Pokok Kepolisian No. 13 /1961 tanggal 19 Juni 1961 merupakan sejarah Kepolisian R.l yang sangat penting sebagai realisasi cita – cita yang selalu menjiwai kehidupan Korps Kepolisian Negara seirama dengan gelora perjuangan rakyat. Setelah pergantian pimpinan Polisi dari Menteri Muda Kepolisian R.S. Soekanto oleh Sukarno Djoyo Negoro mantan Kepala Kepolisian Jawa Timur, kemudian disusul reorganisasi kepolisian yaitu tentang susunan dan tugas kepolisian tingkat departemen.</p>
					<p style="text-align: justify;">Dalam reorganisasi ini Dinas Lalu Lintas / PNUK dimasukkan dalam Korps Polisi Tugas Umum termasuk didalamnya Perintis Polisi Wanita dan Polisi Umum, tanpa mengurangi tugas – tugas Dinas Lalu Lintas sebelumnya :</p>
					<ol style="text-align: justify;">
						<li>Perubahan itu tertuang dalam Peraturan Sementara JM Menteri/KSAK tanggal 31 Desember 1961.</li>
						<li>Tanggal 23 Nopember 1962 dikeluarkan pula peraturan JM Menteri/KSK No. 2.PRT/KK/62 dibentuk kembali Dinas Lalu Lintas, yang terpisah dari Polisi tugas Umum, sedangkan PNUK tetap dimasukkan dalam jajaran Polisi Tugas Umum.</li>
						<li>Tanggal 14 Februari 1964 dengan Surat Keputusan JM MEN PANGAK No. Pol.:11/SK/MK/64 Dinas Lalu Lintas diperluas kembali statusnya menjadi Direktorat       Lalu Lintas. Dengan Surat Keputusan ini maka untuk pertama kali reorganisasi kepolisian bidang lalu lintas menggunakan nama Direktorat Lalu Lintas di tingkat pusat.</li>

					</ol>
					<p style="text-align: justify;">Dalam perkembangan selanjutnya, bekerja sama dengan Departemen Perhubungan Darat dan Direktorat Pendidikan dan Latihan telah dirintis pendidikan kejuruan kader-kader Polantas. Kelanjutan dari kerja sama ini adalah, dikirimnya beberapa Perwira Polisi ke Amerika yaitu Northwestern University Of Traffic Institute (NUTI) dan California High Way Patrol di Sacrament (USA) untuk memperluas pengetahuannya di bidang lalu lintas.</p>
					<p style="text-align: justify;">Dengan kembalinya para perwira yang mengikuti tugas belajar di Amerika, mulailah dirintis untuk pertama kalinya pendidikan Bintara Patroli Jalan Raya (PJR) di Sukabumi tahun 1962 yang diikuti oleh 40 siswa Polisi Lalu Lintas Komisaris di P. Jawa dan Bali. Dan mulai pula Kesatuan Lalu Lintas mengembangkan sayapnya guna memenuhi tuntutan jaman dengan membentuk kesatuan-kesatuan PJR. Pembentukan kesatuan memerlukan perlengkapan yang cukup, dan hal ini dipenuhi dengan bantuan dari pemerintah Amerika Serikat seperti kendaraan bermotor (Jeep dan sedan Falcon dan Chevy) serta alat-alat komunikasi radio (motorola), sepeda motor Harley Davidson.</p>
					<p style="text-align: justify;">Adanya kesatuan PJR didalam tubuh Polri/ Polantas, merupakan suatu organ baru yang sangat menunjang dan sangat diperlukan, baik untuk keamanan, dan penegakan hukum serta penyidikan kecelakaan lalu lintas, tugas-tugas tindakan pertama pada kejahatan maupun bantuan taktis dapat dilaksanakan.</p>
					<p style="text-align: justify;">Karena Perkembangan situasi politik, hubungan diplomatik Indonesia dengan Amerika Serikat mulai memburuk kemudian Polri lepas hubungan dengan Amerika Serikat, sehingga bantuan terputus.</p>
					<p style="text-align: justify;">Bidang pendidikan masyarakat lalu lintas mulai dikembangkan, Polisi Lalu Lintas mulai membuat majalah, mengenalkan cara berlalu lintas pada pramuka dan membentuk Patroli Keamanan Sekolah (PKS). Karena kecelakaan lalu lintas sudah mulai menjadi masalah, Polisi Lalu Lintas mulai mengadakan penerangan-penerangan kepada masyarakat tentang tata cara berlalu lintas yang baik dan benar.</p>
					<p style="text-align: justify;">Pada periode ini mulai muncul usaha yang kuat untuk menyusun Undang-undang lalu lintas dan angkutan jalan untuk menggantikan VWO tahun 1933 peninggalan Belanda. Tahun 1965 berhasil menyusun Undang-undang Lalu Lintas dan Angkutan Jalan Raya No. 3Tahun 1965. Kegiatan-kegiatan Polantas terus dikembangkan, tugas operasional Polisi Lalu Lintas tidak terbatas hanya berkaitan dengan lalu lintas saja, tetapi juga yang berkaitan dengan fungsi lain seperti ikut membantu penindakan terhadap kejahatan, penculikan, kebakaran dan lain-lain. Disamping itu dalam setiap penyelenggaraan kegiatan yang bersifat internasional di Indonesia Polisi Lalu Lintas selalu berperan aktif. Sebagai contoh penyelenggaraan kegiatan olah raga bulu tangkis</p>
					<p style="text-align: justify;">Dalam kegiatan seperti ini Polisi Lalu Lintas memberi andil cukup penting dalam hal tugas pengaturan lalu lintas, pengamanan jalan, pengawalan, agar tetap lancar. Peran Polantas lainnya dalam kegiatan olah raga internasional adalah dalam penyelenggaraan Asean Games IV, Sea Games dan beberapa kegiatan olah raga lainnya.</p>
					
				</div>

