					<h4 class="mt-3 mb-3"><b>4. Periode 1965 - 1998</b></h4>
					<p style="text-align: justify;">Munculnya gerakan G 30 S/PKI pada tanggal 30 September 1965 menuntut segenap alat negara untuk bersatu dengan kokoh, meskipun cukup alot, integrasi Polri ke tubuh ABRI akhirnya dapat berlangsung. Keterpaduan ABRI dan Polisi diharapkan menjadi kekuatan Hankam yang tangguh untuk menghalau setiap pemberontakan dan pengacau yang mengancam keamanan negara dan bangsa Indonesia. Integrasi ABRI dengan Polri di kongkritkan dengan Keppres no. 79/1969 yang berisi Pembagian dan Penentuan Fungsi Hankam. Meskipun berbeda dengan angkatan perang yang terdiri dari AD, AU dan AL tetapi Polri menjadi bagian dari Departemen Hankam. Dengan Keppres tersebut Polri kembali mengadakan penyesuaian-penyesuaian dan perubahan-perubahan dalam tubuh organisasi baik di tingkat pusat maupun daerah. Demikian halnya di kesatuan Polisi Lalu Lintas.</p>
