					<h4 class="mt-3 mb-3 text-center" style="color: #003A91;"><b>POLANTAS ERA DIGITAL: E-POLICING SAAT INI</b></h4>

					<hr>
					<p style="text-align: justify;">Merencanakan dan menyiapkan bagi polantas dalam era digital dengan menerapkan e-policing pada fungsi lalu lintas melalui ERI, SDC, SSC. Pemolisian di bidang lalu lintas perlu membuat model pemolisian yang merupakan penjabaran dari e-policing dan sebagai strategi membangun pemolisian di era digital yang mencakup</p>
					<ol style="text-align: justify ;">
						<li>
							<b><em>Electronic Regident (ERI)</em></b> adalah sistem pendataan regident secara elektronik yang dikerjakan pada bagian BPKB sebagai landasan keabsahan kepemilikan dan asa-usul kendaraan bermotor. Yang dilanjutkan pada bagian STNK dan TNKB sebagai legitimasi pengoperasionalan. TNKB dapat dibangun melalui automatic number plate recognation (ANPR). Dari database kendaraan yang dibangun secara elektronik akan saling berkaitan dengan fungsi kontrol dan forensik kepolisian serta memberikan pelayanan prima. Dari ERI ini dapat dikembangkan menjadi program-program pembatasan pengoperasionalan kendaraan bermotor seperti Electronic Road Pricing (ERP), Electronic Toll Collect (ETC), e-parking, e-banking (dapat menerobos dan memangkas birokrasi samsat), dan Electronic Law Enforcement (ELE).

						</li>
						<li>
							<b><em>Safety Driving Centre (SDC)</em></b> adalah sistem yang dibangun untuk menangani pengemudi dan calon pengemudi kaitannya dengan SIM dengan sistem-sistem elektronik. Sistem ini akan terkait dengan ERI (yang bisa dikembangkan dalam RIC/regident centre), yang dapat digunakan sebagai bagian dari fungsi dasar regident (memberi jaminan legitimasi kompetensi untuk SIM), fungsi kontrol, forensik kepolisian dan pelayanan prima kepolisian).

						</li>
						<li>
							<b><em>Safety and Security Centre (SSC)</em></b> merupakan sistem-sistem elektronik yang mengakomodir pelayanan kepolisian dibidang lalu lintas khususnya yang berkaitan dengan keamanan dan keselamatan, yang diselenggarakan oleh Subdit Gakkum, Dikyasa, dan Subdit Kamsel. Dari sistem data dan sistem-sistem jaringan informasi yang akan dapat dikerjakan oleh TMC (Traffic Management Centre).

						</li>
						<li>
							<b><em>Traffic Management Centre (TMC)</em></b> merupakan pusat komando pengendalian, komunikasi, koordinasi dan informasi (K3i) guna memberikan pelayanan cepat (quick response time) yang dapat mengedepankan Sat-PJR, Sat-Pamwal, Sat-Gatur bahkan petugas-petugas Satlantas tingkat Polres maupun Polsek.

						</li>
					</ol>
				</div>
				
